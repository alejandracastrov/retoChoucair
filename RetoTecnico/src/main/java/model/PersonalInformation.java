package model;

public class PersonalInformation {

    private String name;
    private String last_name;
    private String email;
    private String month_of_birth;
    private String day_birth;
    private String year_birth;
    private String operative_system;
    private String version_operative_system;
    private String language;
    private String password;
    private String password_confirmation;
    private String welcome;


    public String getWelcome() {
        return welcome;
    }

    public void setWelcome(String welcome) {
        this.welcome = welcome;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMonth_of_birth() {
        return month_of_birth;
    }

    public void setMonth_of_birth(String month_of_birth) {
        this.month_of_birth = month_of_birth;
    }

    public String getDay_birth() {
        return day_birth;
    }

    public void setDay_birth(String day_birth) {
        this.day_birth = day_birth;
    }

    public String getYear_birth() {
        return year_birth;
    }

    public void setYear_birth(String year_birth) {
        this.year_birth = year_birth;
    }

    public String getOperative_system() {
        return operative_system;
    }

    public void setOperative_system(String operative_system) {
        this.operative_system = operative_system;
    }

    public String getVersion_operative_system() {
        return version_operative_system;
    }

    public void setVersion_operative_system(String version_operative_system) {
        this.version_operative_system = version_operative_system;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPassword_confirmation() {
        return password_confirmation;
    }

    public void setPassword_confirmation(String password_confirmation) {
        this.password_confirmation = password_confirmation;
    }
}
